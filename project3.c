#include <stdio.h>

int main() {
    int M;
    
    // Введення розміру квадратної матриці
    printf("Введіть розмір квадратної матриці (M): ");
    scanf("%d", &M);

    // Оголошення матриці
    double A[M][M];
    
    // Введення елементів матриці
    printf("Введіть елементи матриці A[%d][%d]:\n", M, M);
    for(int i = 0; i < M; i++) {
        for(int j = 0; j < M; j++) {
            printf("A[%d][%d] = ", i, j);
            scanf("%lf", &A[i][j]);
        }
    }

    // Виведення початкової матриці
    printf("\nПочаткова матриця:\n");
    for(int i = 0; i < M; i++) {
        for(int j = 0; j < M; j++) {
            printf("%8.2f ", A[i][j]);
        }
        printf("\n");
    }

    // Обробка матриці: віднімання останнього рядка
    for(int i = 0; i < M-1; i++) {        // Для всіх рядків, крім останнього
        for(int j = 0; j < M; j++) {      // Для всіх стовпців
            A[i][j] -= A[M-1][j];         // Віднімаємо елемент останнього рядка
        }
    }

    // Виведення результату
    printf("\nРезультуюча матриця:\n");
    for(int i = 0; i < M; i++) {
        for(int j = 0; j < M; j++) {
            printf("%8.2f ", A[i][j]);
        }
        printf("\n");
    }

    return 0;
}